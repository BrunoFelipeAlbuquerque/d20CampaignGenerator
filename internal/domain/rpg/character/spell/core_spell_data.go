func mustBuildCoreSpells(seeds []spellSeed) map[SpellID]Spell {
	spells := make(map[SpellID]Spell, len(seeds))

	for _, seed := range seeds {
		spell, ok := NewSpell(
			seed.id,
			seed.school,
			seed.descriptors,
			seed.components,
			seed.castingTime,
			seed.spellRange,
			seed.targetEffect,
			seed.duration,
			seed.savingThrow,
			seed.spellResistance,
		)
		if !ok {
			panic("invalid core spell seed")
		}

		if _, ok := spells[spell.GetID()]; ok {
			panic("duplicate core spell seed")
		}

		spells[spell.GetID()] = spell
	}

	return spells
}
